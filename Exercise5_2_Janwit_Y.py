# สูตรในการคำนวณหาระยะทางที่เคลื่อนที่ได้ ต่อ 1 หน่วยเวลา
# สูตร v=s/t

print('">>ต้องการทราบว่ารถของคุณมีอัตราความเร็วเท่าไหร่<<"')
print('">โปรดกรอกข้อมูล<"')
s = float(input("รถวิ่งระยะทาง(km): "))
t = float(input("ใช้เวลา(h): "))
v = float(s / t)
print("อัตราความเร็วของรถคุณคือ:",v,"km/h")
print(">>>ซื้อรถใหม่เถอะครับ, เรามีโปรโมชั่นดีๆมานำเสนอ!<<<")